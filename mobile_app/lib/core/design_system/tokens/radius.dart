/// Sacred Radiance border-radius tokens.
/// Soft and elegant — no sharp corners, no excessive rounding.
class SacredRadius {
  SacredRadius._();

  static const double sm   = 4;     // 0.25 rem — small chips, badges
  static const double def  = 8;     // 0.5 rem  — buttons, inputs (default)
  static const double md   = 12;    // 0.75 rem — list tiles, small cards
  static const double lg   = 16;    // 1 rem    — standard cards
  static const double xl   = 24;    // 1.5 rem  — feature / hero cards
  static const double full = 9999;  // pill shape — status indicators only
}
